    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int itemID = Integer.parseInt(txtItemID.getText());
        int customerID = Integer.parseInt(txtCusID.getText());
        String itemName = txtItemName.getText();
        int buyingQty = Integer.parseInt(txtBuyingQty.getText());
        double unitPrice = Double.parseDouble(txtItemPrice.getText());
        double total = unitPrice * buyingQty;
        
        int rowIndex = isAlreadyExists(String.valueOf(itemID));
        
        if (rowIndex == -1) {
            Object[] row = {itemID, customerID, itemName, buyingQty, unitPrice, total};
            dtm.addRow(row);
        } else {
            buyingQty += (int) dtm.getValueAt(rowIndex, 3);
            total = buyingQty * unitPrice;
            tblOrder.setValueAt(buyingQty, rowIndex, 3);
            tblOrder.setValueAt(total, rowIndex, 5);
        }
        

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnBuyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyActionPerformed
        int itemID = Integer.parseInt(txtItemID.getText());
        int customerID = Integer.parseInt(txtCusID.getText());
        String orderType = String.valueOf(cmbOrderType.getSelectedItem());
        
        OrderDTO order = new OrderDTO(0, customerID, orderType, "Confirm", "Pending");
        
        for (int i = 0; i < dtm.getRowCount(); i++) {
            
            String itemId = dtm.getValueAt(i, 0).toString();
            int qty = Integer.parseInt(dtm.getValueAt(i, 3).toString());
            double unitPrice = Double.parseDouble(dtm.getValueAt(i, 4).toString());
            
            OrderDetailDTO orderDetail = new OrderDetailDTO(0, 0, Integer.parseInt(itemId), qty, unitPrice);
            order.getOrderDetails().add(orderDetail);
        }
        
        try {
            boolean purchaseOrder = new OrderController().purchaseOrder(order);
            if (purchaseOrder) {
                dtm.setRowCount(0);
                new QueueForm().setVisible(true);
                JOptionPane.showMessageDialog(this, "Order Placed");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (SQLException ex) {
            Logger.getLogger(OrderForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(OrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnBuyActionPerformed

