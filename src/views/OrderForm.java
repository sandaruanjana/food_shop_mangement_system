    private int isAlreadyExists(String itemCode) {
        
        for (int i = 0; i < dtm.getRowCount(); i++) {
            int code = (int) dtm.getValueAt(i, 0);
            String valueOf = String.valueOf(code);
            if (valueOf.equals(itemCode)) {
                return i;
            }
        }
        
        return -1;
        
    }

