    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = tblCustomer.getSelectedRow();
        int cid = Integer.parseInt(tblCustomer.getValueAt(row, 0).toString());
        String name = txtCusName.getText();
        String address = txtCusAddress.getText();
        int number = Integer.parseInt(txtCusNumber.getText());
        String gender = String.valueOf(cmbCustomerGender.getSelectedItem());

        CustomerDTO customer = new CustomerDTO(cid, name, address, number, gender);

        try {
            boolean updateCustomer = new CustomerController().updateCustomer(customer);
            if (updateCustomer) {
                loadCustomer();
                JOptionPane.showMessageDialog(this, "Customer Update Successfull");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            int row = tblCustomer.getSelectedRow();
            int cid = Integer.parseInt(tblCustomer.getValueAt(row, 0).toString());

            boolean removeCustomer = new CustomerController().removeCustomer(String.valueOf(cid));

            if (removeCustomer) {
                loadCustomer();
                txtCusName.setText("");
                txtCusAddress.setText("");
                txtCusNumber.setText("");
                JOptionPane.showMessageDialog(this, "Customer Delete Successfull");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

