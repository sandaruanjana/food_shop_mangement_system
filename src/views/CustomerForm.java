    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String name = txtCusName.getText();
        String address = txtCusAddress.getText();
        int number = Integer.parseInt(txtCusNumber.getText());
        String gender = String.valueOf(cmbCustomerGender.getSelectedItem());

        CustomerDTO customer = new CustomerDTO(0, name, address, number, gender);

        try {
            boolean addCustomer = new CustomerController().addCustomer(customer);

            if (addCustomer) {
                loadCustomer();
                txtCusName.setText("");
                txtCusAddress.setText("");
                txtCusNumber.setText("");
                JOptionPane.showMessageDialog(this, "Customer Register Successfull");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        int row = tblCustomer.getSelectedRow();
        txtCusName.setText(tblCustomer.getValueAt(row, 1).toString());
        txtCusAddress.setText(tblCustomer.getValueAt(row, 2).toString());
        txtCusNumber.setText(tblCustomer.getValueAt(row, 3).toString());
        cmbCustomerGender.getModel().setSelectedItem(tblCustomer.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tblCustomerMouseClicked

